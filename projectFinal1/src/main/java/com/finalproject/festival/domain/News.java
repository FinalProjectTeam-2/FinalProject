package com.finalproject.festival.domain;

public class News {
	
	private int newsno;
	private String newstitle;
	private String newscontent;
	private String newsimage;
	private String newsuploaddate;
	private String newswriter;
	
	public int getNewsno() {
		return newsno;
	}
	public void setNewsno(int newsno) {
		this.newsno = newsno;
	}
	public String getNewstitle() {
		return newstitle;
	}
	public void setNewstitle(String newstitle) {
		this.newstitle = newstitle;
	}
	public String getNewscontent() {
		return newscontent;
	}
	public void setNewscontent(String newscontent) {
		this.newscontent = newscontent;
	}
	public String getNewsimage() {
		return newsimage;
	}
	public void setNewsimage(String newsimage) {
		this.newsimage = newsimage;
	}
	public String getNewsuploaddate() {
		return newsuploaddate;
	}
	public void setNewsuploaddate(String newsuploaddate) {
		this.newsuploaddate = newsuploaddate;
	}
	public String getNewswriter() {
		return newswriter;
	}
	public void setNewswriter(String newswriter) {
		this.newswriter = newswriter;
	}
	

}
